// Objeto Math
console.log(Math.abs(-3)) // abs -> Transforma o número para positivo.
console.log(Math.ceil(4.2)) // ceil -> Arredonda o valor para cima
console.log(Math.floor(7.9)) // floor -> Arrendonda o valor para baixo
console.log(Math.pow(3, 2)) // pow -> Retorna a base elevada ao expoente.
console.log(Math.round(2.7)) // round -> Arredonda o valor para o inteiro mais proximo (acima de 0.5)
console.log(Math.sqrt(16)) // sqrt -> Mostra a raiz quadrada de um número